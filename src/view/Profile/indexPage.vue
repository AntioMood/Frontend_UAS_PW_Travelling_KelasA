<template>
  <div class="col-lg-4">
    <div class="card card-profile mb-4">
      <div class="card-header" style="background-image: url(https://therichpost.com/wp-content/uploads/2021/05/bootstrap5-carousel-slider-img1.jpg);"> </div>
      <div class="card-body text-center"><img class="card-profile-img" src="https://therichpost.com/wp-content/uploads/2021/03/avatar2.png" alt="Jassa Rich">
        <h3 class="mb-3">{{ users.name }}</h3>
        <p class="mb-4">{{ users.email }}</p>
      </div>
    </div>
  </div>

  <form class="card mb-4">
    <div class="card-header">
      <h4 class="card-heading">Edit Profile</h4>
    </div>
    <div class="card-body">
      <div class="row">
        <div class="col-md-5">
          <div class="mb-4">
            <label class="form-label">Username</label>
            <input class="form-control" type="text" placeholder="Company" value="Papa Ganteng">
          </div>
        </div>
        <div class="col-sm-6 col-md-3">
          <div class="mb-4">
            <label class="form-label">Password</label>
            <input class="form-control" type="text" placeholder="Username" value="SIGANTENG">
          </div>
        </div>
        </div>
      </div>
    <div class="card-footer text-end">
      <button class="btn btn-primary" type="submit">Update Profile</button>
    </div>
  </form>
</template>

<script>
  import axios from "axios";
  import { useRoute  } from 'vue-router'
  import { onMounted, reactive } from "vue";
  export default {
    setup() {
      //reactive state
      const users = reactive({
        name: '',
        email: '',
      })
      //vue route
      const route = useRoute()

      onMounted(() => {
        //get API from Laravel Backend
        axios.get(`http://localhost:8000/api/users/${route.params.id}`)
          .then((response) => {
            //assign state posts with response data
            users.name = response.data.data.name,
            users.email = response.data.data.email
          })
          .catch((error) => {
            console.log(error.response.data);
          });
      });

      return{
            users,
            route
      }
    }
  }
</script>